# Entrada
numero_1 = float(input('Digite um número: '))
numero_2 = float(input('Digite outro número: '))
numero_3 = float(input('Digite outro número: '))

# Processamento
soma = numero_1 + numero_2
diferenca = numero_2 - numero_3

# Saida
print (f'A soma entre {numero_1} e {numero_2} é {soma}, e a diferença entre {numero_2} e {numero_3} é {diferenca}.')
